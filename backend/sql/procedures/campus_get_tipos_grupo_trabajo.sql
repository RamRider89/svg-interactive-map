CREATE PROCEDURE [dbo].[campus_get_tipos_grupo_trabajo] (@idPosicion INT)
AS
BEGIN
SET NOCOUNT ON;
-- ================================================================================================================================================
    IF (@idPosicion > 0)
        BEGIN
            SELECT * FROM CampusTiposGrupoTrabajo WHERE id = @idPosicion;
        END
    ELSE
        BEGIN
            SELECT * FROM CampusTiposGrupoTrabajo;
        END

END