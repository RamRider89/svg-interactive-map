CREATE PROCEDURE [dbo].[campus_get_tipo_trabajo_user] (@idTipo INT)
AS
BEGIN
SET NOCOUNT ON;
-- ================================================================================================================================================
    IF (@idTipo > 0)
        BEGIN
            SELECT * FROM CampusTipoTrabajoUsers WHERE id = @idTipo;
        END
    ELSE
        BEGIN
            SELECT * FROM CampusTipoTrabajoUsers;
        END

END