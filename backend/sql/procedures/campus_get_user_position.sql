CREATE PROCEDURE [dbo].[campus_get_user_position] (@idEmpleado INT)
AS
BEGIN
SET NOCOUNT ON;
-- ================================================================================================================================================
    IF (@idEmpleado > 0)
        BEGIN
            SELECT * FROM CampusUsersPositions WHERE idEmpleado = @idEmpleado;
        END
    ELSE
        BEGIN
            SELECT NULL;
        END

END